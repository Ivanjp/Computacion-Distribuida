    private void sendGet(String user, String psw, String residencia, String sexo, String edad) throws Exception {

        String url = "http://localhost:8000/api";
        String params = "op=reg&usuario=" + user + "&clave=" + psw + "&resid=" + residencia + "&sexo=" + sexo + "&edad=" + edad;
        HttpURLConnection httpClient = (HttpURLConnection) new URL(url + "?" + params).openConnection();

        // optional default is GET
        httpClient.setRequestMethod("GET");
        //
        int responseCode = httpClient.getResponseCode();
        if (responseCode == 200) {
            JOptionPane.showMessageDialog(null, "Registro exitoso!!");
            Login h = new Login();
            h.setVisible(true);
            this.dispose();
        } else if (responseCode == 404) {
            JOptionPane.showMessageDialog(null, "Faltan datos.");
        } else if (responseCode == 412) {
            JOptionPane.showMessageDialog(null, "Verifica tus datos, el nombre de usuario no debe de contener espacios.");
        }

    }
