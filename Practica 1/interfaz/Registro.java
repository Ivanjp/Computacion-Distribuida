    private void botonAgregarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarUsuarioActionPerformed
        // TODO add your handling code here:
        String username = nombreUsuario.getText();
        String p = new String(password.getPassword());
        String res = (String) residencias.getSelectedItem();
        String e = (String) edad.getSelectedItem();
        String s = "";

        if (sHombre.isSelected()) {
            s = "Hombre";
        } else if (sMujer.isSelected()) {
            s = "Mujer";
        }

        try {
            if (username.contains(" ")) {
                JOptionPane.showMessageDialog(null, "El nombre de usuario no puede tener espacios.");
            } else if (username.equals("") || p.equals("") || s.equals("")) {
                JOptionPane.showMessageDialog(null, "No puedes dejar espacios en blanco.");
            } else {
                this.sendGet(username, p, res, s, e);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocurrio un error intentalo de nuevo.");
            Logger.getLogger(Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botonAgregarUsuarioActionPerformed

